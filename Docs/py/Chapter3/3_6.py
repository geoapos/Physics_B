"""
Άσκηση 3.6

Φυσικό φως που διαδίδεται στον αέρα συναντά λεία επιφάνεια πάγου.
Η ανακλώμενη ακτίνα βρέθηκε ολικώς πολωμένη. Να προσδιορίσετε
τη γωνία πρόσπτωσης. Ο δείκτης διάθλασης του πάγου είναι 1,309.
"""

from math import atan, degrees

# Δεδομένα
n = 1.309  # Δείκτης διάθλασης πάγου

print()
print(f'Από το νόμο του Brewster έχουμε: n = tan(θ) => tan(θ) ={n} => θ=', end='')
θ = degrees(atan(n))
print(f'{θ:.2f}°')