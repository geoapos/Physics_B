'''Άσκηση 38:

Διαφορά δυναμικού 120V εφαρμόζεται σε δύο παράλληλες μεταλλικές πλάκες.
Εάν το πεδίο που παράγεται μεταξύ των πλακών είναι 600V/m,
πόσο απέχουν οι δύο πλάκες;
'''

# Δεδομένα

E = 600  # Ένταση ηλεκτρικού πεδίου σε V/m.
