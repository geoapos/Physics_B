'''Άσκηση 37:

Δύο παράλληλες μεταλλικές πλάκες απέχουν απόσταση 0,5cm και είναι συνδεδεμένες
με διαφορά δυναμικού 80V. Να βρεθεί η ένταση του ηλεκτρικού πεδίου μεταξύ αυτών.
'''

# Δεδομένα

l = 0.5 * 10 ** (-2)  # απόσταση οπλισμών σε m.
V = 80  # Τάση σε V

print()
E = V / l
print('Η ένταση δίνεται από την σχέση: E=V/l => E={:.0f} V/m'.format(E))
